    private void openProjectActionPerformed(java.awt.event.ActionEvent evt) {                                            
        File filelocation = null;
        String location = "";
        if(projectLocation.getText().equals(sMainFrame.getProject().getLocation())){
        location = sMainFrame.getProject().getLocation();
        filelocation = new File(location);
        }
        if (filelocation.isDirectory() && filelocation.exists() && new File(location+"/.project").exists())
        {        
         DesktopApi.open(filelocation);
        }
    }                                           

    private void renameProjectActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if (!sMainFrame.getProject().getName().equals(projectName.getText().trim())) {
            if (Validator.isValidName(projectName.getText().trim())) {
                if(sMainFrame.renameProject(projectName.getText().trim())){
                    Notification.show("Project Renamed successfully");
                }else{
                    Notification.show("Couldn't rename the Project");
                }
            } else {
                Notification.show("Invalid Name " + projectName.getText());
            }
        }
    }                                             



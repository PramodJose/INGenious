    public void loadForCurrentProject() {

        projectName.setText(sMainFrame.getProject().getName());
        projectLocation.setText(sMainFrame.getProject().getLocation());
        noOfScenarios.setText(sMainFrame.getProject().getScenarios().size() + "");
        int testCaseCount = 0;
        int reusableCaseCount = 0;
        for (int i = 0; i < sMainFrame.getProject().getScenarios().size(); i++) {
            testCaseCount += sMainFrame.getProject().getScenarios().get(i).getTestcaseCount();
            reusableCaseCount += sMainFrame.getProject().getScenarios().get(i).getReusableCount();
        }
        noOfTestCases.setText(String.valueOf(testCaseCount));
        noOfReusables.setText(String.valueOf(reusableCaseCount));
        int tdCount = 0;
        for (TestData testData : sMainFrame.getProject().getTestData().getAllEnvironments()) {
            tdCount += testData.getTestDataList().size();
        }
        noOfTestData.setText(String.valueOf(tdCount));
        openProject.setEnabled(new File(sMainFrame.getProject().getLocation()).exists());
    }

